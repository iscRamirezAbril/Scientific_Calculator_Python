# Creation of a class called "Substraction"
class Substraction():
    # Class constructor
    def __init__(self, Num1, Num2):
        self.Num1 = Num1
        self.Num2 = Num2
    
    # Class method
    def SubsOp(self, Num1, Num2):
        print("The answer for the substitution is: ", Num1 - Num2)

