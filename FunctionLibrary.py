# Creation of a class called "Addition"
class Addition():
    # Class constructor
    def __init__(self, Num1, Num2):
        self.Num1 = Num1
        self.Num2 = Num2

    # Class method
    def AddOp(self, Num1, Num2):
        print("The answer for the addition is: ", Num1 + Num2)

